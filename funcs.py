class Funcs:

    def __init__(self, a, b):
        self.a = a
        self.b = b

    def __repr__(self):
        return f'I am a class Funcs. Perform arithmetic operations with numbers a and b'

    def sum_sum(self):
        return self.a + self.b

    def subtraction(self):
        return self.a / self.b

    def division(self):
        return self.a - self.b

    def multiplication(self):
        return self.a * self.b
